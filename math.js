// arredondamento com floor
let poderThor =  9.7;

let poderThorArredondado = Math.floor(poderThor);

console.log('Poder Thor', poderThor);
console.log('Poder Thor Arredondado', poderThorArredondado);

// arredondamento com ceil

console.log('Poder Thor', poderThor);
console.log('Poder Thor Arredondado', Math.ceil(poderThor));


