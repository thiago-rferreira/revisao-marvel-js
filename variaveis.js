let heroi = "Homem-Aranha"; // Com let nós podemos mudar
console.log(heroi); // Mostra Homem-Aranha
heroi = "Capitão América"; // Agora o valor de heroi é Capitão América
console.log(heroi); // Mostra Capitão América

// Com const nós não podemos mudar
const hero = "Homem-Aranha"; // Com const nós não podemos mudar
console.log("Com const", hero); // Mostra Homem-Aranha
// hero = "Capitão América"; // Apresenta um erro 'Assignment to constant variable.'

